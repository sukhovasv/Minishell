#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char *const	c1 = (unsigned char *)s1;
	unsigned char *const	c2 = (unsigned char *)s2;
	size_t					i;

	i = 0;
	while (i < n)
	{
		if (c1[i] != c2[i])
			return (c1[i] - c2[i]);
		i++;
	}
	return (0);
}
